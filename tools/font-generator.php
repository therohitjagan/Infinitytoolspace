                    </div>
                `;
                return;
            }
            
            container.innerHTML = '';
            recentlyCopied.forEach((item, index) => {
                const itemElement = document.createElement('div');
                itemElement.className = 'copied-item';
                itemElement.innerHTML = `
                    <div class="text-truncate me-2">${item}</div>
