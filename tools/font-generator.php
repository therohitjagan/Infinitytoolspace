        // Share text (through navigator.share if available)
        function shareText() {
            const text = document.getElementById('outputText').innerText;
            
            if (navigator.share) {
                navigator.share({
                    title: 'Fancy Text',
                    text: text
                })
                .catch(error => console.log('Error sharing:', error));
