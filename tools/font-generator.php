        // Update new font preview
        function updateNewFontPreview() {
            const text = document.getElementById('inputText').value || 'Sample Text';
            const upperChars = document.getElementById('upperChars').value;
            const lowerChars = document.getElementById('lowerChars').value;
            
            if (upperChars.length === 26 && lowerChars.length === 26) {
                const chars = upperChars + lowerChars;
                let result = '';
                
                for (let i = 0; i < text.length; i++) {
                    const char = text[i];
                    const upperIndex = char.toUpperCase().charCodeAt(0) - 65;
                    const lowerIndex = char.toLowerCase().charCodeAt(0) - 97;
                    
                    if (upperIndex >= 0 && upperIndex < 26 && char === char.toUpperCase() && char.toLowerCase() !== char.toUpperCase()) {
                        result += chars[upperIndex];
                    } else if (lowerIndex >= 0 && lowerIndex < 26 && char === char.toLowerCase() && char.toLowerCase() !== char.toUpperCase()) {
                        result += chars[26 + lowerIndex];
                    } else {
                        result += char;
