        // Update all font previews
        function updateAllFonts() {
            const text = document.getElementById('inputText').value || 'Sample Text';
            const charCount = text.length;
            document.getElementById('charCount').innerText = charCount + ' characters';
            
            // Update previews for built-in fonts
            for (const fontName in fonts) {
                const preview = document.getElementById('preview-' + fontName);
                if (preview) {
                    preview.innerText = convertText(text, fontName);
                }
            }
            
            // Update previews for custom fonts
            for (const fontName in customFonts) {
                const preview = document.getElementById('preview-' + fontName);
                if (preview) {
                    preview.innerText = convertTextCustom(text, fontName);
                }
            }
            
            // Update new font preview
            updateNewFontPreview();
