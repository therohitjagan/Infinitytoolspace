        // Convert text using built-in fonts
        function convertText(text, fontName) {
            if (!fonts[fontName]) return text;
            
            let result = '';
            for (let i = 0; i < text.length; i++) {
                const char = text[i];
                const upperIndex = char.toUpperCase().charCodeAt(0) - 65; // A = 0, B = 1, etc.
                const lowerIndex = char.toLowerCase().charCodeAt(0) - 97; // a = 0, b = 1, etc.
                
                if (upperIndex >= 0 && upperIndex < 26 && char === char.toUpperCase() && char.toLowerCase() !== char.toUpperCase()) {
                    result += fonts[fontName][upperIndex];
                } else if (lowerIndex >= 0 && lowerIndex < 26 && char === char.toLowerCase() && char.toLowerCase() !== char.toUpperCase()) {
                    result += fonts[fontName][26 + lowerIndex];
                } else {
                    result += char;
                }
            }
            return result;
        }
        
        // Convert text using custom fonts
        function convertTextCustom(text, fontName) {
            if (!customFonts[fontName]) return text;
            
            let result = '';
            for (let i = 0; i < text.length; i++) {
                const char = text[i];
                const upperIndex = char.toUpperCase().charCodeAt(0) - 65;
                const lowerIndex = char.toLowerCase().charCodeAt(0) - 97;
                
                if (upperIndex >= 0 && upperIndex < 26 && char === char.toUpperCase() && char.toLowerCase() !== char.toUpperCase()) {
                    result += customFonts[fontName][upperIndex];
                } else if (lowerIndex >= 0 && lowerIndex < 26 && char === char.toLowerCase() && char.toLowerCase() !== char.toUpperCase()) {
                    result += customFonts[fontName][26 + lowerIndex];
                } else {
                    result += char;
                }
            }
            return result;
        }
        
        // Copy text to clipboard
        function copyText() {
            const outputText = document.getElementById('outputText').innerText;
            navigator.clipboard.writeText(outputText)
                .then(() => {
                    // Show feedback
                    const originalText = document.getElementById('outputText').innerText;
                    document.getElementById('outputText').innerHTML = '<span class="text-success"><i class="fas fa-check-circle me-2"></i>Copied to clipboard!</span>';
                    
