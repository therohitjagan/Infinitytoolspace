        // Save to recently copied list
        function saveToRecentlyCopied(text) {
            // Add to the beginning of the array
            recentlyCopied.unshift(text);
            
            // Keep only the last 5 items
            if (recentlyCopied.length > 5) {
                recentlyCopied.pop();
            }
            
            // Save to localStorage
            localStorage.setItem('recentlyCopied', JSON.stringify(recentlyCopied));
            
            // Update the UI
            updateRecentlyCopiedUI();
        }
        
        // Update recently copied UI
        function updateRecentlyCopiedUI() {
            const container = document.getElementById('recentlyCopied');
            
            if (recentlyCopied.length === 0) {
                container.innerHTML = `
                    <div class="text-center text-muted">
