        // Select a font and update output
        function selectFont(fontName) {
            const text = document.getElementById('inputText').value || 'Sample Text';
            let output = '';
            
            if (customFonts[fontName]) {
                output = convertTextCustom(text, fontName);
            } else {
                output = convertText(text, fontName);
            }
            
            document.getElementById('outputText').innerText = output;
            
            // Highlight the selected font card
            const fontCards = document.querySelectorAll('.font-card');
            fontCards.forEach(card => card.classList.remove('active'));
            
